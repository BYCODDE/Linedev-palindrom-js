function myFunc(str) {
  if (str.split("").reverse().join("").toLowerCase() == str.toLowerCase()) {
    console.log(" рЃћрЃА рЃАрЃбрЃарЃўрЃюрЃњрЃў рЃърЃљрЃџрЃўрЃюрЃцрЃарЃЮрЃЏрЃў");
  } else {
    console.log(" рЃћрЃА рЃАрЃбрЃарЃўрЃюрЃњрЃў рЃљрЃа рЃљрЃарЃўрЃА рЃърЃљрЃџрЃўрЃюрЃцрЃарЃЮрЃЏрЃў");
  }
}

